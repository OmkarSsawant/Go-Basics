package main

import "fmt"

// > has no pointer arithmatic like C
// ex : p++ => forwarded us to next array value .no such thing in Go

func main() {
	i, j := 42, 2701

	p := &i         // point to i
	fmt.Println(*p) // read i through the pointer
	*p = 21         // set i through the pointer
	fmt.Println(i)  // see the new value of i

	p = &j         // point to j
	*p = *p / 37   // divide j through the pointer
	fmt.Println(j) // see the new value of j

	//derefrence pointer
	p = nil
}
